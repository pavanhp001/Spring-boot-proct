package com.A.V.beans.job;

import java.util.Date;

import com.A.V.beans.entity.User;

 

public class JobStatus {

	private User user;
	private Date start;
	private Date end;
	private ScheduledOutcome status;
	
	
	

	public User getUser() {
		return user;
	}

	public void setUser(User user) {
		this.user = user;
	}

	public Date getStart() {
		return start;
	}

	public void setStart(Date start) {
		this.start = start;
	}

	public Date getEnd() {
		return end;
	}

	public void setEnd(Date end) {
		this.end = end;
	}

	public ScheduledOutcome getStatus() {
		return status;
	}

	public void setStatus(ScheduledOutcome status) {
		this.status = status;
	}

	 

}
