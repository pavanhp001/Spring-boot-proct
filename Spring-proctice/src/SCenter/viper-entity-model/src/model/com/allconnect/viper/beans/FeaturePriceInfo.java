package com.A.V.beans;

import java.io.Serializable;
import javax.persistence.Embeddable;

/**
 * @author Earl Baugh
 *
 */

@Embeddable
public class FeaturePriceInfo implements Serializable
{
    private static final long serialVersionUID = 5958764545394L;

    private double baseNonRecurringPrice;
    private double baseRecurringPrice;
    private String baseNonRecurringPriceUnit;
    private String baseRecurringPriceUnit;
    private double revenue;

    /**
     * Default Constructor.
     */
    public FeaturePriceInfo()
    {
        // Do nothing right now.
    }
    
    
    public final double getBaseNonRecurringPrice()
    {
        return baseNonRecurringPrice;
    }

    public final void setBaseNonRecurringPrice( final double baseNonRecurringPrice )
    {
        this.baseNonRecurringPrice = baseNonRecurringPrice;
    }


    public final double getBaseRecurringPrice()
    {
        return baseRecurringPrice;
    }


    public final void setBaseRecurringPrice( final double baseRecurringPrice )
    {
        this.baseRecurringPrice = baseRecurringPrice;
    }


    public String getBaseNonRecurringPriceUnit() 
    {
        return baseNonRecurringPriceUnit;
    }


    public void setBaseNonRecurringPriceUnit( final String baseNonRecurringPriceUnit )
    {
        this.baseNonRecurringPriceUnit = baseNonRecurringPriceUnit;
    }


    public String getBaseRecurringPriceUnit() 
    {
        return baseRecurringPriceUnit;
    }


    public void setBaseRecurringPriceUnit( final String baseRecurringPriceUnit ) 
    {
        this.baseRecurringPriceUnit = baseRecurringPriceUnit;
    }


    public double getRevenue() 
    {
        return revenue;
    }


    public void setRevenue( final double revenue ) 
    {
        this.revenue = revenue;
    }
}
