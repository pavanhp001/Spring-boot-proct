package com.A.V.beans;

import javax.persistence.Basic;
import javax.persistence.Column;
import javax.persistence.Embeddable;

/**
 * @author gdesai
 * 
 */

@Embeddable
public class ProductChoiceBase
{

    @Basic( optional = false )
    @Column( nullable = false )
    private String externalId;

    private String vendorExternalId;

    private String name;

    private String shortDescription;

    private String longDescription;

    private double baseRecurringPrice;

    private double baseNonRecurringPrice;

    private String textAnswer;

    private int displayOrder;
    
    public String getExternalId()
    {
        return externalId;
    }

    public void setExternalId( final String externalId )
    {
        this.externalId = externalId;
    }

    public String getVendorExternalId()
    {
        return vendorExternalId;
    }

    public void setVendorExternalId( final String vendorExternalId )
    {
        this.vendorExternalId = vendorExternalId;
    }

    public String getName()
    {
        return name;
    }

    public void setName( final String name )
    {
        this.name = name;
    }

    public String getShortDescription()
    {
        return shortDescription;
    }

    public void setShortDescription( final String shortDescription )
    {
        this.shortDescription = shortDescription;
    }

    public String getLongDescription()
    {
        return longDescription;
    }

    public void setLongDescription( final String longDescription )
    {
        this.longDescription = longDescription;
    }

    public double getBaseRecurringPrice()
    {
        return baseRecurringPrice;
    }

    public void setBaseRecurringPrice( final double baseRecurringPrice )
    {
        this.baseRecurringPrice = baseRecurringPrice;
    }

    public double getBaseNonRecurringPrice()
    {
        return baseNonRecurringPrice;
    }

    public void setBaseNonRecurringPrice( final double baseNonRecurringPrice )
    {
        this.baseNonRecurringPrice = baseNonRecurringPrice;
    }

    public String getTextAnswer()
    {
        return textAnswer;
    }

    public void setTextAnswer( final String textAnswer )
    {
        this.textAnswer = textAnswer;
    }

    public int getDisplayOrder()
    {
        return displayOrder;
    }

    public void setDisplayOrder( final int displayOrder )
    {
        this.displayOrder = displayOrder;
    }

}
