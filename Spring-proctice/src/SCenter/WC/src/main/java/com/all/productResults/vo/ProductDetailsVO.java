package com.A.productResults.vo;

public class ProductDetailsVO extends ProductSummaryVO {

	private String featureBlk;
	private String promotionBlk;
	private String image;
	private String longDescription;
	private String priceBlk;
	private String marketingHighlightsBlk;
	
	public String getFeatureBlk() {
		return featureBlk;
	}
	public void setFeatureBlk(String featureBlk) {
		this.featureBlk = featureBlk;
	}
	public String getPromotionBlk() {
		return promotionBlk;
	}
	public void setPromotionBlk(String promotionBlk) {
		this.promotionBlk = promotionBlk;
	}
	public String getImage() {
		return image;
	}
	public void setImage(String image) {
		this.image = image;
	}
	public String getLongDescription() {
		return longDescription;
	}
	public void setLongDescription(String longDescription) {
		this.longDescription = longDescription;
	}
	public String getPriceBlk() {
		return priceBlk;
	}
	public void setPriceBlk(String priceBlk) {
		this.priceBlk = priceBlk;
	}
	public String getMarketingHighlightsBlk() {
		return marketingHighlightsBlk;
	}
	public void setMarketingHighlightsBlk(String marketingHighlightsBlk) {
		this.marketingHighlightsBlk = marketingHighlightsBlk;
	}
	


}
