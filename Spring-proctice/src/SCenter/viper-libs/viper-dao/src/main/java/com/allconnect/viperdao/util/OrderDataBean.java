package com.A.Vdao.util;

import java.util.Calendar;
import java.util.Date;

public class OrderDataBean {
	private String customerId;
	private String orderId;
	private String lineItemId;
	private String productId;
	private String productName;
	private String providerExternalId;
	private Calendar scheduledInstallDate;
	private Calendar orderDate;
	private Calendar lineItemCreateDate;
	
	/**
	 * @return the customerId
	 */
	public String getCustomerId() {
		return customerId;
	}
	/**
	 * @param customerId the customerId to set
	 */
	public void setCustomerId(String customerId) {
		this.customerId = customerId;
	}

	/**
	 * @return the orderId
	 */
	public String getOrderId() {
		return orderId;
	}
	/**
	 * @param orderId the orderId to set
	 */
	public void setOrderId(String orderId) {
		this.orderId = orderId;
	}
	/**
	 * @return the lineItemId
	 */
	public String getLineItemId() {
		return lineItemId;
	}
	/**
	 * @param lineItemId the lineItemId to set
	 */
	public void setLineItemId(String lineItemId) {
		this.lineItemId = lineItemId;
	}
	/**
	 * @return the productId
	 */
	public String getProductId() {
		return productId;
	}
	/**
	 * @param productId the productId to set
	 */
	public void setProductId(String productId) {
		this.productId = productId;
	}
	/**
	 * @return the productName
	 */
	public String getProductName() {
		return productName;
	}
	/**
	 * @param productName the productName to set
	 */
	public void setProductName(String productName) {
		this.productName = productName;
	}
	/**
	 * @return the providerExternalId
	 */
	public String getProviderExternalId() {
		return providerExternalId;
	}
	/**
	 * @param providerExternalId the providerExternalId to set
	 */
	public void setProviderExternalId(String providerExternalId) {
		this.providerExternalId = providerExternalId;
	}
	
	/**
	 * @return the scheduledInstallDate
	 */
	public Calendar getScheduledInstallDate() {
		return scheduledInstallDate;
	}
	/**
	 * @param scheduledInstallDate the scheduledInstallDate to set
	 */
	public void setScheduledInstallDate(Calendar scheduledInstallDate) {
		this.scheduledInstallDate = scheduledInstallDate;
	}
	/**
	 * @return the orderDate
	 */
	public Calendar getOrderDate() {
		return orderDate;
	}
	/**
	 * @param orderDate the orderDate to set
	 */
	public void setOrderDate(Calendar orderDate) {
		this.orderDate = orderDate;
	}
	/**
	 * @return the lineItemCreateDate
	 */
	public Calendar getLineItemCreateDate() {
		return lineItemCreateDate;
	}
	/**
	 * @param lineItemCreateDate the lineItemCreateDate to set
	 */
	public void setLineItemCreateDate(Calendar lineItemCreateDate) {
		this.lineItemCreateDate = lineItemCreateDate;
	}
	@Override
	public String toString() {
		return "OrderDataBean [customerId=" + customerId + ", orderId="
				+ orderId + ", lineItemId=" + lineItemId + ", productId="
				+ productId + ", productName=" + productName + ", providerExternalId="
				+ providerExternalId + ", scheduledInstallDate=" + scheduledInstallDate.toString() + "]";
	}
	

}
